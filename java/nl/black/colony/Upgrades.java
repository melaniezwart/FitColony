public class Upgrades {

	private double foodProduction = 1;
	private double foodConsumption = 1;
	private double defence = 1;
	private double offense = 1;
	private double engineeringSpeed = 1;
	private double livingSpace = 1;

	public double getFoodProduction() {
		return foodProduction;
	}

	public void setFoodProduction(double foodProduction) {
		this.foodProduction = foodProduction;
	}

	public double getFoodConsumption() {
		return foodConsumption;
	}

	public void setFoodConsumption(double foodConsumption) {
		this.foodConsumption = foodConsumption;
	}

	public double getDefence() {
		return defence;
	}

	public void setDefence(double defence) {
		this.defence = defence;
	}

	public double getOffense() {
		return offense;
	}

	public void setOffense(double offense) {
		this.offense = offense;
	}

	public double getEngineeringSpeed() {
		return engineeringSpeed;
	}

	public void setEngineeringSpeed(double engineeringSpeed) {
		this.engineeringSpeed = engineeringSpeed;
	}

	public double getLivingSpace() {
		return livingSpace;
	}

	public void setLivingSpace(double livingSpace) {
		this.livingSpace = livingSpace;
	}
}
